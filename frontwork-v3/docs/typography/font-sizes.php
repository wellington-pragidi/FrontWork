		<code class="fw_orange bg0 ft17">fit</code> é ajustavel.<br>
		Os números seguintes não tem significado com o tamanho, é só uma referência.
	</p>
	<p>
		Os tamanhos de fonte (<code>font-size</code>), não tem breakpoint, <code class="fw_orange bg0 ft17">fs</code> por ter sómente tamanhos pequenos e <code class="fw_orange bg0 ft17">fit</code> por ter um excelente cálculo de ajuste que vai diminuindo gradualmente no viewport.
